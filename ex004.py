#Faça um programa que leia algo pelo teclado e mostre na tela o seu tipo primitido
#e todas as informações possiveis sobre ele

a = input('Digite um numero: ')

print(type(a))
print(a.isupper())
print(a.isspace())
print(a.istitle())
print(a.isalnum())
print(a.islower())
print(a.isalpha())
print(a.isdecimal())
print(a.isdigit())
print(a.isnumeric())
print(a.isascii())